// Precious / strategic ores that can be found in mineral-rich surface tiles.
// None = a normal tile with no ore. Everything else is a discoverable, researchable ore.
public enum OreType
{
    None,

    Ferralite,     // common structural metal
    Cuprion,       // conductive copper analogue
    Titanex,       // light strong alloy base
    Aurelium,      // precious, gold analogue
    Argenite,      // precious, silver analogue
    Platinode,     // catalytic platinum-group
    Pyronium,      // volcanic energy crystal
    Cryonite,      // volatile ice-locked fuel
    Helium3,       // fusion fuel from gas giants
    Uranite,       // radioactive fissile ore
    Luminite,      // photonic / light-emitting crystal
    Adamantine,    // ultra-hard armour ore
    Neutronium,    // ultra-dense, exotic
    Quantite,      // quantum-coherent crystal
    Xenocryst      // alien, poorly understood
}
